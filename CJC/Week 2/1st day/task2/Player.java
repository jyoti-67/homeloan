public class Player
{
	int prank;
	String pname;
	
	long pmob;
	double psalary;


	public void setPrank(int prank)
	{
		this.prank = prank;
	}

	public int getPrank()
	{
		return prank;
	}
	public void setPname(String pname)
	{
		this.pname = pname;
	}
	public String getPname()
	{
		return pname;
	}
	public void setPmob(long pmob)
	{
		this.pmob = pmob;
	}
	public long getPmob()
	{
		return pmob;
	}
	public void setPsalary(double psalary)
	{
		this.psalary = psalary;
	}
	public double getPsalary()
	{
		return psalary;
	}
}